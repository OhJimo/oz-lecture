
// 대입 연산자
let x = 10;

// 복합 대입 연산자
x += 10;            // x = x + 10
console.log(x);

let y = 10;
y -= 5;             // y = y - 5
console.log(y);

let z = 10;
z *= 5;             // z = z + 5

let u = 10;
u /= 5;             // u = u / 5
console.log(u);

let v = 10;
v %= 3;             // v = v % 3
console.log(v);

let t = 10;
t **= 3;            // t = t ** 3   // t = 10 X 10 X 10
console.log(t);
